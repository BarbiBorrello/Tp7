    private void agregarAlumno() {
        for (Alumno a1 : alumnos) {

            jComboAlumno.addItem(a1);

        }

    }

    private void agregarMateria() {
        for (Materia m1 : materias) {

            jComboMateria.addItem(m1);

        }
    }


